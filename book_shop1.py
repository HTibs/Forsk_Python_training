orders = [["34587", "Learning Python, Mark Lutz", 4, 40.95], 
      ["98762", "Programming Python, Mark Lutz", 5, 56.80], 
      ["77226", "Head First Python, Paul Barry", 3,32.95],
      ["88112", "Einführung in Python3, Bernd Klein",  3, 24.99]]


def f1(order):
    tempo=[]
    for index,value in enumerate(order, start=0):
        if(index==0):
            tempo.append(value)
        elif(index==2):
            calculator=value
        elif(index==3):
            total = calculator*value
            if(total<100):
                total = total+10
            tempo.append(total)
            
    return tuple(tempo)

temp = list(map(f1,orders))                
           
